public class MovePoint
{

    public int _id;
    public int _key;

    public float _x;

    public float _y;
    public float _dir;

    public MovePoint( int id, int key, float x, float y,float dir)
    {
        this._id = id;
        this._key = key;
        this._x = x;
        this._y = y;
        this._dir = dir;
    }
}

